public class Tenesse {
    /* This project was an introduction to creating methods and how they can be recycled to reduce my workload and the computers runtime of the program. */
    public static void main(String[] args){
        writeT();
        writeE();
        writeN();
        writeN();
        writeE();
        writeS();
        writeS();
        writeE();
        writeE();
    }

    public static void writeT() {
        System.out.println("TTTTTTTTTT");
        System.out.println("    TT    ");
        System.out.println("    TT    ");
        System.out.println("    TT    ");
        System.out.println("    TT    ");
        System.out.println("    TT    ");
        System.out.println("\n");
    }

    public static void writeE(){
        System.out.println("EEEEEEEEEE");
        System.out.println("E");
        System.out.println("EEEEEEEEEE");
        System.out.println("E");
        System.out.println("EEEEEEEEEE");
        System.out.println("\n");
    }

    public static void writeN(){
        System.out.println("N N      N");
        System.out.println("N   N    N");
        System.out.println("N     N  N");
        System.out.println("N        N");
        System.out.println("\n");
    }
    
    public static void writeS(){
        System.out.println(" SSSSSSS ");
        System.out.println("S       S");
        System.out.println("S        ");
        System.out.println("S        ");
        System.out.println(" SSSSSSS ");
        System.out.println("       S ");
        System.out.println("       S ");
        System.out.println(" S     S ");
        System.out.println(" SSSSSSS ");
        System.out.println("\n");
    }
}